#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	char	*res;

	res = s1;
	while (*res)
		res++;
	while (*s2)
		*res++ = *(char*)s2++;
	*res = '\0';
	return (s1);
}
